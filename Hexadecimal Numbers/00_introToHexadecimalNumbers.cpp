#include <iostream>
using namespace std;

int main()
{
    int x = 0x10;
    cout<<x; // 16
    
    // why output is 16?
    // 0x10 is a hexadecimal number (base-16), which means each digit in the number can be one of sixteen possible values, from 0 to 15.

    // Hexadecimal numbers are often used in computer programming because they are a convenient way to represent large numbers.

    // In hexadecimal notation, 0x10 represents the value (1 * 16^1) + (0 * 16^0) = 16. So, the integer variable x is initialized to the value 16.


    return 0;
}

